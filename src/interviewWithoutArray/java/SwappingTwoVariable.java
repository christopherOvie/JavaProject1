package interviewWithoutArray.java;

public class SwappingTwoVariable {

	public static void main(String[] args) {
		
		
	
		int a =6;
		int b =8;
		int temp;  //empty basket
		
		
		
	//basket a  apple
		//basket b  orange
		temp =a;
		a=b;
		b=temp;
		System.out.println(a);
		System.out.println(b);
		
		
		

	}

}
